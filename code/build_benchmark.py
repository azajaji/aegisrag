def write_corpus():
    counts = {"policy": 0, "itdocs": 0, "hr": 0}
    for domain_name, docs in [("policy", POLICY_DOCS), ("itdocs", IT_DOCS), ("hr", HR_DOCS)]:
        domain_dir = ROOT / domain_name
        domain_dir.mkdir(parents=True, exist_ok=True)
        # Clean previous corpus files so removed docs don't linger
        for fp in domain_dir.glob("*.txt"):
            fp.unlink()
        for fname, paragraphs in docs.items():
            (domain_dir / fname).write_text("\n\n".join(paragraphs), encoding="utf-8")
            counts[domain_name] += 1
    return counts


def build_questions():
    benchmark = []
    qid = 0

    def add(questions, domain):
        nonlocal qid
        for q in questions:
            question, gold, doc, para = q
            qid += 1
            entry = {
                "qid": qid,
                "domain": domain,
                "question": question,
                "gold_answer": gold,
                "answerable": gold is not None,
            }
            if gold is not None:
                entry["gold_doc"] = doc
                entry["gold_para_idx"] = para
            benchmark.append(entry)

    add(POLICY_QUESTIONS, "policy")
    add(IT_QUESTIONS, "itdocs")
    add(HR_QUESTIONS, "hr")
    return benchmark


def main():
    counts = write_corpus()
    benchmark = build_questions()
    out = ROOT / "questions" / "benchmark.json"
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(benchmark, indent=2, ensure_ascii=False), encoding="utf-8")

    n_answerable = sum(1 for b in benchmark if b["answerable"])
    n_unans = len(benchmark) - n_answerable
    print(f"Documents written: {counts}")
    print(f"Questions: {len(benchmark)} ({n_answerable} answerable, {n_unans} unanswerable)")
    by_domain = {}
    for b in benchmark:
        by_domain.setdefault(b["domain"], [0, 0])
        if b["answerable"]:
            by_domain[b["domain"]][0] += 1
        else:
            by_domain[b["domain"]][1] += 1
    print(f"Per domain (answerable / unanswerable): {by_domain}")


if __name__ == "__main__":
    main()
